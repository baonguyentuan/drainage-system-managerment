export const configFont = (frimaryFont: string) => {
  let fontGroup: string;
  if (frimaryFont.toLowerCase().indexOf(".vn") !== -1) {
    fontGroup = "tcvn3";
  } else if (frimaryFont.toLowerCase().indexOf("vni") !== -1) {
    fontGroup = "vni";
  } else {
    fontGroup = "unicode";
  }
  return fontGroup;
};
export const convertFont = (
  fontGroupFrom: string[],
  fontGroupTo: string[],
  text: string
) => {
  let arr = text.split("");
  let newArr: string[] = [];
  arr.map((item, index) => {
    let charIndex = fontGroupFrom.findIndex((char) => char === item);
    if (charIndex !== -1) {
      newArr.push(fontGroupTo[charIndex]);
    } else {
      newArr.push(item);
    }
  });
  let newText = newArr.reduce((accumulator, currentValue) => {
    return accumulator + currentValue;
  }, "");
  return newText;
};
